CREATE TABLE `USER` (
   `USER_SEQUENCE`   INT   NOT NULL AUTO_INCREMENT PRIMARY KEY,
   `USER_ID`   VARCHAR(20)   NULL,
   `USER_PASSWORD`   VARCHAR(30)   NULL,
   `USER_NAME`   VARCHAR(30)   NULL,
   `USER_PHONENUM`   VARCHAR(30)   NULL,
   `USER_CARNUM`   VARCHAR(30)   NULL,
   `USER_CARTYPE`   VARCHAR(30)   NULL,
   `USER_CARNUMCOLOR`   VARCHAR(30)   NULL,
   `USER_BANKNAME`   VARCHAR(30)   NULL,
   `USER_ACCOUNT`   VARCHAR(30)   NULL,
   `USER_COURYAREA`   VARCHAR(100)   NULL
);

