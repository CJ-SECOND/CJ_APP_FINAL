   `PICKUP_SEQUENCE`   INT   NOT NULL AUTO_INCREMENT PRIMARY KEY,
   `USER_SEQUENCE`   VARCHAR(20)   NULL,
   `PICKUP_YN`   CHAR(1)   NULL,
   `PICKUP-TIME`   TIMESTAMP   NULL,
   `PICKUP_COUNT`   INT(10)   NULL,
