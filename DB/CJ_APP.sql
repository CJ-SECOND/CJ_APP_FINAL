   `COURY_TO_ADDRESS`   VARCHAR(100)   NULL,
   `COURY_FROM_NAME`   VARCHAR(100)   NULL,
   `COURY_TO_NAME`   VARCHAR(100)   NULL,
   `COURY_FROM_PHONENUM`   VARCHAR(20)   NULL,
   `COURY_TO_PHONENUM`   VARCHAR(20)   NULL,
   `COURY_GETPLACE`   VARCHAR(10)   NULL,
